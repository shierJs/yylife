                    第五条 为避免对信息浏览者造成误导，信息发布者发布二手信息时需确认发布身份，即个人卖家或商家。<br>
                    第六条 个人卖家的定义：信息发布人（公司）不以相关交易作为主要职业（营业范围），不将其交易所得作为主要经济来源。<br>
                    第三章 违规行为认定和处罚<br>
                    第七条 用户发布包括但不限于如下非法信息的，根据情节轻重进行以下一种或多种处理：删除所有违规内容、关闭账号、扣除保金及（或）服务费、终止服务等。<br>
                    （一）发布含有违反中华人民共和国宪法、法律、行政法规、规范性文件以及国家法律法规明令禁止的信息；<br>
                    （二）物品交易过程中使用暴力、恐吓、教唆、欺骗等方式，侵犯他人人身及财产权益。<br>
                    第八条 发布包括但不限于以下列示的有悖社会公序良俗的信息，删除所有违规内容。<br>
                    （一）信息中含有低俗、下流、恶趣味等描述或恶意辱骂、诽谤，人身攻击等内容；<br>
                    （二）买卖、置换、赠送的物品存在违背社会伦理、道德风尚、习俗习惯等情形。<br>
                    第九条 发布包括但不限于以下列示的虚假信息，删除所有违规内容。<br>
                    （一）提供虚假的物品信息；<br>
                    （二）对物品的描述与实际情况不符；<br>
                    （三）二手商家冒充个人卖家进行交易。<br>
                    第十条 发布包括但不限于以下列示的无意义信息（对浏览者无帮助信息），根据情节轻重进行以下一种或多种处理：降权违规信息、返回修改违规信息、删除所有违规内容等。<br>
                    （一）发布的信息与二手类别无关联；<br>
                    （二）发布的信息关键内容缺失或者不规范。<br>
                    第十一条 其他包括但不限于以下列示的侵犯用户权益等行为，根据情节轻重进行以下一种或多种处理：降权违规信息、返回修改违规信息、删除所有违规内容、关闭账号、扣除违约方缴纳的保证金及（或）服务费用、终止服务等。<br>
                    （一）通过各种方式故意注册大量账户或发布大量信息，违规攫取流量，并伤害其他用户利益的行为；<br>
                    （二）交易、服务过程中拒不履行双方约定的行为；<br>
                    （三）违反二手信息填写规范的行为；<br>
                    （四）发布侵犯他人知识产权的信息。

                  </p>
                  <p>第四章 欺诈行为的认定及处罚</p>
                  <p>
                    第十二条 关于欺诈行为的定义：指用户（包含个人卖家及商家）违背沂源生活平台质量标准，通过采取发布虚假信息或者其他不正当手段欺骗、误导其他用户，使其他用户的合法权益受到损害的行为。包括但不仅限于：<br>
                    （一）发布推广信息描述与其提供的商品/服务真实情形不一致<br>
                    （二）发布虚假信息<br>
                    （三）隐瞒商家身份<br>
                    （四）夸大商品属性、配置<br>
                    （五）故意隐瞒、忽略商品真实情况<br>
                    （六）故意告知用户不真实的商品/服务情况<br>
                    （七）进行虚假承诺<br>
                    （八）怠于履行发货义务<br>
                    （九）通过其他手段骗取财物等<br>
