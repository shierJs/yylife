                  <p>第二章 房产行业主体</p>
                  <p>
                    第四条	为避免对信息浏览者造成误导，信息发布者在沂源生活平台发布房产行业信息时需确认房屋来源，即个人房源或中介房源。<br>
                    第五条	个人房东的定义：不以第三方立场（中介）来推动交易或者虽以第三方来推动交易，但不依此来收取租客介绍费用；信息发布人（公司）不把相关交易作为主要职业（经营范围）或经济来源，定义为个人房东。<br>
                    第六条	中介的定义：以营利为目的，或以第三方立场来推动买卖并向交易双方/一方收取费用、以该交易作为主要经济来源，以相关交易作为职业的个人（公司）的定义为中介（商家）。<br>
                    第七条	沂源生活依据用户使用习惯，将房产行业类别分为：租房、合租房、求租房、二手房、短租房/日租房、写字楼、商铺租售/生意转让、厂房/仓库/土地、新房等。<br>
                    第三章 违规行为认定和处罚<br>
                    第八条	用户发布包括但不限于如下非法信息的，根据情节轻重进行以下一种或多种处理：删除所有违规内容、关闭账号、扣除保证金及（或）服务费、终止服务等。<br>
