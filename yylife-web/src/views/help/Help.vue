                    (1)首先要走出一个误区，大家买机器最常看的就是 *#06#来区分产地了，串号的第七、第八位代码表示手机的产地，诺基亚的代码如下：10：芬兰；20：德国；30：韩国；40：北京；60：东莞 ，但是新的nokia手机已经不使用这个编号来区分产地了的，在手机的待机状态下输人＃06＃，手机显示的一串数字即为其主板串号，将其记 下；取下机子电池，看机身后面的两个标签中的白色标签上的串号即机壳串号，另一个标签一般是入网许可证。是否和前面数字一致；再看 机子包装盒上的IMEI号，比较三者，看是否都一致。如果主板串号和机壳串号不一致，那么这台机器很有可能是经过维修或零件拼装的，千 万不能买。如果三个串号都一致，加上有入网许可证的话，基本上就能证明此机是正品，但是也不能百分之百的肯定，因为现在的JS什么都 能造假，主板串号可以用电脑和软件刷写，假的串号标签和入网许可证更是几块钱就可以买到，为慎重起见还应该按下面的方法继续验证。<br>
                    (2)仔细看机身电池卡锁处有无拨痕，sim卡槽和触点有无磨损，MMC卡接触点是否有印痕，全新应该是光亮的。<br>
                    (3)看机子外包装和说明书：正规新机子的包装和说明书印刷精美，多为铜版纸印刷，字体和图像十分清晰、有层次感 ，而假冒的包装盒却多为简单单面纸张仿造，字体和图像都很粗糙，印刷字迹模糊，不少非正规渠道的改装的说明书甚至是复印的。还有一 些翻新机子虽然采用的是原来正品的包装盒和说明书，但毕竟机器是旧的，其包装盒必有磨损，说明书也一定存在使用过的痕迹，购买时多 加留意，就能发现。<br>
                    (4)看入网许可证：真的入网许可证上有水印，表面的颜色不均匀，有深有浅，有粗有细；而假的多为复印，表面颜色 没有变化。如果你看不出的话，还可以用验钞机照一下入网许可证，真的里面有防伪图案会呈现出红色的“CMII”的图案。而假的图案则 模糊不清。真的入网证一般都是用针式打印机打印的，数字清晰，仔细看有针打的凹痕，而假的入网许可证是普通打印机打印的，数字不十 分清晰，没有凹痕。一般而言，入网许可证是比较难造假的，毕竟造假所需的设备在成本方面比较高。另外还可以上网查询来检验入网许可 证的真伪，在此就不详细说明了。<br>
                    (5)看手机外壳：打开一款新机子时会很明显地注意到，在机子“关键部位”都有贴膜做重点防护，例如内屏、外屏， 摄象头等等，这些部位只要接触，就会留下指纹．当您看到新机子上的指纹后，任凭JS如何解释您也不要听信。此外，灰尘和使用痕迹也是 翻新机子同样难以避免的，机子上下外壳之间的缝隙(一般为1MM左右)，键盘周围的空隙，还有充电器插槽等处都是容易“藏污纳垢”的地方 ，翻盖机子的转轴处也会留下使用过的痕迹，同样也要特别注意。 翻新机子一般采用的外壳都是仿原装的，或旧外壳精心处理而成的，所以 机子外壳前后两个部分很容易出现闭合不紧、留缝很大的情况，鉴别方法是用手指甲试试能否插入手机上下盖的接缝，如果能插进去，就是 组装壳。对于没有更换外壳机子，JS出售前用一种像清洁剂的雾剂一喷，旧壳的表面立刻出现一种像新机一样的磨砂效果，若不仔细查看， 便会被JS蒙混过关，但是用不了多长时间，机壳的喷漆就会脱落，购买的时候一定要仔细观察外壳，因为JS的工艺再好，也没有原厂的设备 先进，如果仔细查看的话就能看到一点小颗粒是灰尘，由于喷漆时没有在真空房间作业造成。 如果是翻盖机还可以查看一下翻盖的结合处， 因为有些时候就算JS把机器表面处理得再好，这些地方也难免留下一些灰尘的痕迹。另外查看机身螺丝是否崭新，有没有螺丝刀拧动过的痕 迹。更要注意的是，许多新手机都有一股明显的檀香味，而旧机子和换壳机一般都没有这样的味道，或是有加工时留下的异味，而新机器会 是檀香味道，不同与一般的清洁剂和香水味。机身不能有粘粘的蜡和油(有的话就是被JS美容了)。<br>
                    (6)看屏幕贴纸：全新的应该贴的很好，没有气泡什么地，浑然一体。<br>
                    (7)看手机配件：正规厂家的新机子有全套的原厂配件，至少也得有一电一充。翻新手机的配件一般都不齐全或者不是 原厂的，购机之前最好先去相关的网站了解一下标配的原厂配件都有哪些，以此防止被JS克扣配件。另外，看配套的充电器和耳机的插头上 有划痕，一般旧机肯定充过电，还是能看出不同的，看手机耳塞有没有发黑的，如果有则可能是翻新或者被人使用过的。<br>
                    (8)看记录：看一看手机中的游戏、短信、电话本、通话记录、自编铃声、WAP和语音功能及通话纪录等项目有没有使 用过的记录，如果有的话则有非常大的翻新嫌疑。另外在正常的使用过程中，机子频繁地自动关机或是死机，那么可以肯定地说这款手机的 主板或是软件存在一定问题。<br>
                    (9)看保修：向经销商询问机子的保修期并索要正规的发票。翻新手机无法享受正规手机“三包”规定的所有权益，因 此一般没有保修期或保修期相对较短。如果销售商说他们自己负责保修也不能答应，因为正规手机一般都是全国联保的。如果销售商无法做 到这些，那么手机肯定存在问题。<br>
                    (10)看价格：在购买机子前，最好先在当地报纸或是网站上了解一下手机的价格，如果报出的价格很便宜，就要警惕 了，这样“廉价”的手机不是翻新手机、非正规渠道产品，就是配件“缺斤短两”。<br>
                    如何识别高仿机：<br>
                    我们在开机搜索网络的时候按*#06#，然后在你的手机上会出现一个序列号。国际移动设备串号IMEI，应该是 全世界唯一的（山寨机除外），应该共有15到17位，15位是永远不会改变的，17位的话最后两位在刷机、更新等操作后可能会改变，但是一 般只看前15位，另外 这15位号码应该与机身上贴的进网许可上的一样，和保修、包装上的一样，如果不符的话说明机器有问题，可能是高仿 、水货、翻新机等。 第7、8位是00时 是大陆的行货，原厂正装的。手机的串号，即 IMEI 码它是International Mobile Equipment Identity(国际移动设备身份) 的简称，就像是手机的身份证，是用来帮助辨别手机身份真伪的。每个手机都只有唯一一个串号，不会雷同。 我们一般是将手机上按 “*#06#” 键后显示的串号跟手机盒子上的条形码以及手机背面取下电板后里面的串号对应，如果 3 者一样，是真 行货的可能性大些（不排除二手行货）。 因此，我们可以通过看 IMEI 码分辨真假行货、水货。<br>
