                  <p>提醒小贴士：《劳动合同法》明文规定：用人单位招用劳动者，不得扣押劳动者的居民身份证和其他证件，不得要求劳动者提供担保或者以其他名义向劳动者收取财物。如果招聘单位以任何名义扣押劳动者身份证等证件，立刻报警处理。</p>
                  <p></p>
                  <p>了解职介</p>
                  <p>1、什么是职介</p>
                  <p>合法的营利性职介介绍工作一般只能收取两个项目的费用：求职登记费及推荐成功后的中介服务费，且收费后必须开具有效票据。</p>
                  <p>登记时仅收取小额求职登记费，收取报名费后需在规定时间内为求职者安排一定数量的面试。介绍成功后方可收取中介、服务费。该费用为一次性收费，不能多次收取，收费后必须开具有效票据。被推荐人员在推荐岗位上工作时间过短的（个人原因除外），可要求返还部分中介费，具体依据各地区管理规定实施。</p>
                  <p>任何在介绍成功前收取中介费、服务费、会员费、服装费、建档费等各种费用的均为非法收费行为。</p>
                  <p>具体收费标准可参照国家有关规定自主确定，必需明码标价并接受当地物价部门监督。如收费过高，求职者就要留心了。</p>
                  <p>具体管理办法请参照各省市、地区管理规定。</p>
                  <p></p>
                  <p>如：“上海市浦东新区营利性职业介绍机构收费的暂行规定”中对营利性职业介绍机构的收费项目和标准做了如下规定：</p>
                  <p>一、</p>
                  <p>（1）求职登记报名费：每人超过10元（三个月内介绍面试至少3次）。</p>
                  <p>（2）推荐成功中介费：</p>
                  <p></p>
                  <p>收费标准为：每人按本人月工资收入的30%及以下收取，此费为一次性收费。</p>
                  <p>a、若被推荐人员（非个人原因）的工作不满1个月，按已收费的80%退中介费。</p>
                  <p>b、若被推荐人员（非个人原因）的工作满1个月不满3个月，退中介费20~50%。</p>
                  <p>c、若被推荐人员工作满3个月及以上的，不予退还中介费。</p>
                  <p></p>
                  <p>（3）委托招聘用工：成功1人向用工单位收服务费500元以内。</p>
                  <p></p>
                  <p>（4）高级技术、高级管理人员（相应的高级证书）的收费标准可实行面谈，协商一致后确定。</p>
                  <p></p>
                  <p>二、本区营利性职介机构的上述收费，按国家税务部门规定，开具统一监制的税务收费票据。</p>
                  <p></p>
                  <p>三、各营利性职介机构的收费以公正、自愿为原则，各项收费必须明码标价。公开收费项目和收费标准。严格财务管理。</p>
                  <p></p>
                  <p>3、黑职介防骗</p>
                  <p>选择让中介机构介绍工作时，一定选择信誉好、正规合法的职业介绍机构。</p>
                  <p></p>
                  <p>提防“黑职介”骗钱请一定注意做到以下五点：</p>
                  <p>a、检验资质：到职介所找工作时一定要看清这家职介所是否有“四证”。即：职业介绍准办证、工商营业执照、税务登记证、收费许可证。</p>
                  <p>b、抵制诱惑，拒绝乱收费，保护好个人证件：求职者在找工作时应保持理性,正确评估自己的能力和实力,不要轻信“优厚待遇”,要有自我保护意识,不要相信“交钱保证上班”、“高薪急聘无经验也可”等不实宣传，明显不合理的优厚待遇背后往往存在着陷阱。遇到索要身份证等个人证件的，马上予以拒绝。</p>
                  <p>c、签订书面协议：接受职介服务时，要注意与职业中介签订书面协议,明确双方权利和义务,口头承诺最好也写到合同上。</p>
                  <p>d、索要票据，保存证据：如果求职者交纳了费用，一定要索取正式的发票，上面应加盖职介机构的印章，以便发生纠纷时留作证据。</p>
                  <p>e、及时举报：如果发现对方有欺骗行为, 应及时向工商、劳动保障等相关部门举报投诉,依法维护自己的合法权益。</p>
                  <p>在沂源生活网遇到黑职介，请点击信息页面上的“举报该信息”投诉。</p>
                  <p></p>
                  <p>4、网上识别职介信息</p>
                  <p>具有以下特征的信息可能是职介信息。</p>
                  <p>同一公司或用户发布短时间内发布过多不同类型、不同行业、性质的工作的。</p>
                  <p>同一用户名、公司发布招聘信息使用过多不同电话的。如：同一公司发布的多条信息使用了10几个不同的电话。</p>
                  <p>招聘信息中检验出的电话归属地显示与发布职位的工作地点不符。</p>
                  <p>同一用户名发布多个公司信息。</p>
                  <p>注意：中介、职介信息不等于虚假信息，但求职者对待中介信息也需要谨慎，建议您上网查询，了解中介的资质、信誉，以免受到黑中介的蒙骗。</p>
