                    联系人是信息内容的发布者和信息意图的表达者，真实有效的联系人身份是信息质量的根基，信息发布者出于保护隐私的需要，可以使用姓氏与“先生”、“女士”、“老师”、“小姐”等称谓进行搭配，但必须保证用户的性别与其称谓相符合，避免对信息浏览者造成不必要的误解。信息发布者也可以根据自己的偏好，使用个性化的称谓或昵称。我们要求信息发者使用本人真实身份进行信息的发布，保证通过联系方式能够联系到信息发布者本人，同时不允许发布包括但不限于以下情形的联系方式：<br>
                    （1）含有违反国家法律法规以及国家法律法规明令禁止的词汇表述；<br>
                    （2）违反社会公序良俗，辱骂、诽谤他人的词汇表述；<br>
                    （3）隐晦、容易令人产生歧义的词汇表述；<br>
                    （4）对信息本身做广告性描述的词汇表述。<br>
                    5.关于内容<br>
                    内容是信息的主体部分，是对信息发布者所发布产品、服务的详尽描述。用户可从本地服务企业的发展状况、业务范围、发展潜力等方面进行如实描述。用户也可根据自己的偏好，进行个性化语言描述。我们鼓励用户发布真实有效的信息内容，同时不允许发布包括但不限于以下情形的内容：<br>
                    （1）含有违反国家法律法规以及国家法律法规明令禁止的信息描述；<br>
                    （2）违反社会公序良俗的信息描述；<br>
                    （3）超出本地服务类别允许发布信息的范围，与本地服务类别信息无关的信息描述；<br>
                    （4）虚假、伪造、夸大事实的信息描述；<br>
                    （5）表达隐晦、含糊、语意不清，容易让信息浏览者产生歧义的信息描述；<br>
                    （6）内容含有无关字符，对信息浏览者正常浏览信息造成干扰的信息描述。<br>
                    6.关于价格（该类别的价格多指服务、加盟费用，为了表述方便，统称为价格）<br>
                    价格是产品、服务价值的最终体现，也是双方达成交易的最终标尺。我们要求信息发布者如实表达自己的心理价位，不允许通过虚假的价格误导信息浏览者。标注的价格是信息发布者心理价位与市场价位的统一，任何违背市场经济规律、违反市场价格趋势进行的价格标注，我们有理由怀疑其动机是否超出市场主体的正当经济行为。我们鼓励信息发布者真实表述心理价位，同时不允许出现包括但不限于以下情形：<br>
                    （1）标注价格与实际交易价格偏离一般市场价格波动范围（双方另有约定除外）；<br>
                    （2）价格表述含糊，价格单位与价格数字之间不匹配；<br>
                    （3）价格项中的价格表述与其他项中的价格表述不一致；<br>
                    （4）不规范的价格书写方式（用字母“o”代替数字“0”，用字母“l”代替数字“1”等情形）。<br>
                    7.关于地址<br>
                    地址是指信息发布者的所在地或产品、服务的所在地，涉及到本地服务类别，特指企业、商家的所在地。我们要求信息发布者对地址项进行详实描述，能够精确到具体的街道、楼层、门牌号、摊位号，不伪造地址、不冒用地址，同时不允许发布包括但不限于以下情形的地址信息：<br>
                    （1）盗用、冒用他人地址；<br>
                    （2）描述的地址虚假。<br>
