                    （三）招聘过程中使用暴力、恐吓、教唆、欺骗等方式，侵犯他人人身及财产权益。<br>
                    第十条	发布包括但不限于以下列示的有悖社会公序良俗的信息，删除所有违规内容。<br>
                    （一）信息中含有低俗、下流、恶趣味等描述或恶意辱骂、诽谤、人身攻击等内容；<br>
                    （二）信息内容违背正常伦理、道德、社会风俗习惯等。<br>
