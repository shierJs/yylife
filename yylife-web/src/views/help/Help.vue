                  <p>第一章 概述</p>
                  <p>
                    第一条	为了保护沂源生活招聘与求职用户的合法权益，根据《沂源生活信息质量标准总则》，结合沂源生活打击沂源生活平台内招聘行业欺诈行为的具体经验及实际情况，制定沂源生活招聘行业信息质量标准细则（以下简称“本细则”）。<br>
                    第二条	本细则主要针对沂源生活常规招聘行业和兼职招聘行业违规行为的认定和处罚、招聘信息发布规范、简历发布规范、面试邀请规范等进行了规定。<br>
                    第三条	本细则为《沂源生活信息质量标准总则》的有效组成部分，本细则中的所有术语、简称、定义，如无特殊说明，与总则中规定的相同。<br>
