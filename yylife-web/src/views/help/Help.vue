                  <p>第二章	招聘行为实施方式</p>
                  <p>
                    第四条	企业直招：是指企业不通过其他第三方机构，以自己名义直接在沂源生活平台发布招聘信息。<br>
                    第五条	职业中介：是指经政府主管部门审核批准具有法人资质、被特许经营的、为用人单位招用人员和劳动者求职提供中介服务及其他相关服务的经营性组织，包括但不限于人力资源服务机构、职业介绍所、保安服务机构、船员服务机构、猎头公司等。<br>
                    第六条	劳务派遣：劳务派遣机构是指经政府主管部门审核批准具有法人资质、被特许经营劳务派遣业务的机构，其与劳动者之间订立劳动合同并支付报酬，把劳动者派向其他实际用工单位，再由实际用工单位向劳务派遣机构支付服务费用的一种用工形式。<br>
                    第七条	定向委培：定向培训是指用人单位根据生产服务岗位的技能要求，委托社会培训机构对其准备招聘的人员在上岗前实施的职业技能培训，培训考核合格后，由企业择优录用。<br>
                    第八条	其他符合中国法律法规、中国承认或加入的国际条约及沂源生活平台相关协议及规则的招聘行为。
                  </p>
                  <p>第三章	违规行为的认定和处罚</p>
                  <p>
                    第九条	用户发布包括但不限于如下非法信息的，根据情节轻重进行以下一种或多种处理：删除所有违规内容、关闭账号、扣除保证金及（或）服务费、终止服务等。<br>
                    （一）发布含有违反中华人民共和国宪法、法律、行政法规、规范性文件以及国家法律法规明令禁止的职位、信息；<br>
                    （二）无资质发布国家法律法规对招聘方有明确资质要求的职位；<br>
                    （三）招聘过程中使用暴力、恐吓、教唆、欺骗等方式，侵犯他人人身及财产权益。<br>
                    第十条	发布包括但不限于以下列示的有悖社会公序良俗的信息，删除所有违规内容。<br>
                    （一）信息中含有低俗、下流、恶趣味等描述或恶意辱骂、诽谤、人身攻击等内容；<br>
                    （二）信息内容违背正常伦理、道德、社会风俗习惯等<br>
                    第十一条 发布包括但不限于以下列示的虚假信息，删除所有违规内容。<br>
                    （一）招聘信息中，关键内容（含企业名称、职位名称、职位类别、工作地址、联系方式、薪酬福利等）填写虚假；<br>
                    （二）发布销售、推广、转让等与建立劳动关系无关的信息或服务；<br>
                    （三）招聘与公司性质以及经营范围无关联的职位；<br>
                    （四）职介故意隐藏身份，以企业直招名义发布其所代理的招聘职位，职介公司为自身公司招聘人员，按照企业直招认定。<br>
                    第十二条 发布包括但不限于以下列示的无意义信息（对浏览者无帮助信息），根据情节轻重进行以下一种或多种处理：降权违规信息、返回修改违规信息、删除所有违规内容等。<br>
                    （一）故意发布与招聘类别不相关的职位；<br>
                    （二）招聘信息中，关键内容（含企业名称、职位名称、职位类别、工作地址、联系方式等）填写不全或者不规范。<br>
                    第十三条 其他包括但不限于以下列示的侵犯用户权益的行为，根据情节轻重进行以下一种或多种处理：降权违规信息、返回修改违规信息、删除所有违规内容、关闭账号、扣除保证金及（或）服务费、终止服务等。<br>
                    （一）未经授权使用他人品牌进行招聘的行为；<br>
                    （二）通过各种方式故意注册大量账户或发布大量信息，违规攫取流量，并伤害其他用户利益的行为；<br>
                    （三）交易、服务过程中拒不履行双方约定的行为；<br>
                    （四）	违反招聘信息填写规范的行为。<br>
                    （五）以计算机系统等技术手段批量下载简历，违规获取其他用户个人信息。
