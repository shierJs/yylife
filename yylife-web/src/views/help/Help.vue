                  <p>第四章 附则</p>
                  <p>
                    附则一 房产信息填写规范<br>
                    1.关于标题<br>
                    标题是所有信息内容主旨的全部体现，表达信息发布者的意图。言简意赅、表词达意的标题文字更容易受到潜在消费群体的青睐。我们鼓励信息发布者如实、恰当、富有技巧地为信息内容拟一个合适的标题，同时不允许发布包括但不限于以下情形的标题：<br>
                    （1）含有违反国家法律法规以及国家法律法规明令禁止的标题描述；<br>
                    （2） 超出房产行业允许发布信息的范围，与房产行业信息无关的标题描述；<br>
                    （3）表达模糊、无法判断信息发布者意图的标题描述；<br>
                    （4）标题中含有无关符号、字母、数字等内容且影响浏览者判断其真实意图的标题描述；<br>
                    （5）夸大事实，容易对信息浏览者产生误导的标题描述。<br>
                    2.关于图片<br>
                    图片是浏览者最直观了解信息详情的入口，图片的质量影响浏览者对信息的判断，在一定程度上，浏览者的偏好受图片的左右。我们鼓励信息发布者上传真实、清晰、合理尺寸的图片，同时不允许发布包括但不限于以下情形的图片：<br>
                    （1）图片内容含有违反国家法律法规以及国家法律法规明令禁止的内容；<br>
                    （2）图片内容与房产行业的信息无关联；<br>
                    （3）未经他人许可，盗用、冒用他人的图片；<br>
                    （4）发布所有权存在纠纷、争议的图片；<br>
                    （5）图片模糊、难以辨认，影响浏览者对信息内容的判断；<br>
                    （6）上传多张与信息内容无关联的图片，影响浏览者对信息内容的判断；<br>
                    （7）图片中所含有的联系方式等内容占据图片的面积过大影响浏览者对信息内容的判断；<br>
                    （8）图片中所含有的联系方式与信息所留联系方式不一致。<br>
                    3.关于联系方式<br>
                    联系方式在买家和卖家之间起纽带作用，电话号码、QQ、微信等主流通讯工具均属于联系方式的范畴，信息发布者在信息发布时可根据自己的意愿选择一种或多种联系方式进行填写。为了保证能够与信息发布者及时取得联系，我们要求真实、有效的电话号码为必填选项，同时不允许发布包括但不限于以下情形的联系方式：<br>
                    （1）盗用、冒用他人联系方式；<br>
                    （2）联系方式虚假、不存在（电话号码、QQ、微信虚假或不存在）；<br>
                    （3）联系方式不规范书写（用字母“o”代替数字“0”，用字母“l”代替数字“1”等情形）；<br>
                    （4）联系方式中含有非法含义的字符或拼音。<br>
                    4.关于联系人<br>
                    联系人是信息内容的发布者和信息意图的表达者，真实有效的联系人身份是信息质量的根基，信息发布者出于保护隐私的需要，可以使用姓氏与“先生”、“女士”、“老师”、“小姐”等称谓进行搭配，但必须保证用户的性别与其称谓相符合，避免对信息浏览者造成不必要的误解。信息发布者也可以根据自己的偏好，使用个性化的称谓或昵称。我们要求信息发者使用本人真实身份进行信息的发布，保证通过联系方式能够联系到信息发布者本人，同时不允许发布包括但不限于以下情形的联系方式：<br>
                    （1）盗用、冒用他人联系方式；<br>
                    （2）联系方式虚假、不存在（电话号码、QQ、微信虚假或不存在）；<br>
                    （3）联系方式不规范书写（用字母“o”代替数字“0”，用字母“l”代替数字“1”等情形）；<br>
                    （4）联系方式中含有非法含义的字符或拼音。<br>
                    5.关于内容<br>
                    内容是信息的主体部分，是对信息发布者所发布产品、服务的详尽描述。用户可从本地服务企业的发展状况、业务范围、发展潜力等方面进行如实描述。用户也可根据自己的偏好，进行个性化语言描述。我们鼓励用户发布真实有效的信息内容，同时不允许发布包括但不限于以下情形的内容：<br>
                    （1）含有违反国家法律法规以及国家法律法规明令禁止的信息描述；<br>
                    （2）违反社会公序良俗的信息描述；<br>
                    （3）超出本地服务类别允许发布信息的范围，与本地服务类别信息无关的信息描述；<br>
                    （4）虚假、伪造、夸大事实的信息描述；<br>
                    （5）表达隐晦、含糊、语意不清，容易让信息浏览者产生歧义的信息描述；<br>
                    （6）内容含有无关字符，对信息浏览者正常浏览信息造成干扰的信息描述。<br>
                    6.关于价格（该类别的价格多指服务、加盟费用，为了表述方便，统称为价格）<br>
                    价格是产品、服务价值的最终体现，也是双方达成交易的最终标尺。我们要求信息发布者如实表达自己的心理价位，不允许通过虚假的价格误导信息浏览者。标注的价格是信息发布者心理价位与市场价位的统一，任何违背市场经济规律、违反市场价格趋势进行的价格标注，我们有理由怀疑其动机是否超出市场主体的正当经济行为。我们鼓励信息发布者真实表述心理价位，同时不允许出现包括但不限于以下情形：<br>
                    （1）标注价格与实际交易价格偏离一般市场价格波动范围（双方另有约定除外）；<br>
                    （2）价格表述含糊，价格单位与价格数字之间不匹配；<br>
                    （3）价格项中的价格表述与其他项中的价格表述不一致；<br>
                    （4）不规范的价格书写方式（用字母“o”代替数字“0”，用字母“l”代替数字“1”等情形）。<br>
                    7.关于地址<br>
                    地址是指信息发布者的所在地或产品、服务的所在地，涉及到本地服务类别，特指企业、商家的所在地。我们要求信息发布者对地址项进行详实描述，能够精确到具体的街道、楼层、门牌号、摊位号，不伪造地址、不冒用地址，同时不允许发布包括但不限于以下情形的地址信息：<br>
                    （1）盗用、冒用他人地址；<br>
                    （2）描述的地址虚假。<br>
                    如有其他未尽事宜，请参照《沂源生活信息质量标准总则》及沂源生活相关线上协议、规则等规定进行处理。<br>
