                  <p>如何识别中介冒充个人</p>
                  <p>
                    1、看用户发帖记录<br>
                    在详情页，点击联系人后面的名称，可以查看用户在网站的发帖纪录，如果该用户在短时间内发布了多处不同房源，基本可以判定为中介。<br>
                    2、看电话<br>
                    标题、图片、内容、小区名称中带有与联系方式中不同的电话号码，特别是用空格、符号、谐音等各种方式隐藏真实电话号码的，90%为中介。结合搜索引擎中搜到的该电话信息判断，如搜到该电话在其他网站发布的不同房源信息，经纪人网上店铺等，可以判定为中介。<br>
                    3、看标题<br>
                    一般房东写的标题较为简单，而经纪人帖子可能会写的比较吸引人，如好房不等人、稀缺房源、性价比高、精品推荐、免费看房等等。<br>
                    4、看房屋介绍<br>
                    描述简单，内容泛泛的多为经纪人，房东的的信息一般会写的比较详细，具体的行车路线，房源的突出特点等。<br>
                    5、看价格<br>
                    价格过低可能为虚假房源，有些经纪人发布一些地段好，价格便宜的房源吸引人，当有人表示对此房有意向时，却开始推荐其他房源；另外，也有些黑中介发布低价格的房源吸引客户，收取看房费。如何识别黑中介？<br>
                    6、看付款方式<br>
                    同意月付或押一付一，基本上都是房东。<br>
                    7、打电话询问<br>
                    打电话时，可以通过询问房屋的详细信息与发布的信息是否一致来判断，也可以直接询问对方是否为经纪人。<br>
                  </p>
                  <p>选择中介公司要点</p>
                  <p>
                    一般而言，能够开办中介业务的机构必须是具备公司法规定的条件，领取工商营业执照、具备地产经纪资格的机构才是合法有效的地产中介。根据《城市房地产中介服务管理》规定，设立房地产中介服务机构应具备下列条件：<br>
                    （一）有自己的名称、组织机构；<br>
                    （二）有固定的服务场所；<br>
                    （三）有规定数量的财产和经费； <br>
                    （四）从事房地产咨询业务的，具有房地产及相关专业中等以上学历、初级以上专业技术职称人员须占总人数的50%以上；从事房地产评估业务的，须有规定数量的房地产估价师；从事房地产经纪业务的，须有规定数量的房地产经纪人。<br>
                    1、选择知名品牌的中介公司<br>
                    大型的品牌中介公司注意诚信经营宗旨，有完善的制度及透明的操作流程，能有效保障客户及业主利益。<br>
                    2、看中介资质要留意细节<br>
                    除注意公司品牌外，在进入一家中介门店时，也可以通过细节判断该中介的状况。<br>
                    a.铺面整洁统一（大部分正规中介其分行都有统一的格局形式）。 <br>
                    b.注意中介的营业执照（正规的中介公司都有政府部门颁发的营业执照，并出示于明显位置，以便客户清楚了解该中介的法定注册公司名称、法人代表等必要情况）。 <br>
                    c.经纪资格证（一般来说，每间中介公司门店必须具备4个以上持有资格证的经纪人，买卖双方达成交易后，必须是持牌经纪人方可签约）。<br>
                    3、熟悉交易流程<br>
                    正规公司会以书面、海报、宣传单张等形式告知客户房产交易详细流程，包括：查册、签三方合同、晒图、递件、交税、过户、取证等等，并且向客户明示费用的收取明细，尤其在收取每一笔费用时都出示盖有公司法定公章的收据或发票。
                  </p>
                  <p>如何识别黑中介</p>
                  <p>
                    1、收费没有凭证<br>
                    正规公司都可以出具盖有公司法定公章的收据或发票，如果不开，这个费用的收取就没有得到相关管理单位的认可。<br>
                    《城市房地产中介服务管理》规定：<br>
                    第十八条 房地产中介服务费用由房地产中介服务机构统一收取，房地产中介服务机构收取费用应当开具发票，依法纳税。<br>
                    2、虚假房源<br>
                    黑中介发布质优价廉的房源，吸引看房人，随后推辞该房屋已经租出，带您看别的房子，乘机强收看房费、信息费。<br>
                    3、收押金<br>
                    黑中介在客户找房时声称有合适的房子，随后让对方交“押金”。当客户要求陪同看房时，他们通常会推托房东有事不能来，或带客户看一些烂房子。如此一拖再拖，“赚”押金。<br>
                    4、假房东<br>
                    先租到房子，后冒充房东转租，从此失踪。<br>
                    5、免佣金<br>
                    宣称不收佣金，背后提高房租，吃巨额差价。
                  </p>
                  <p>租房注意事项</p>
                  <p>
                    1、明确询问此套房源是否存在<br>
                    有些经纪人发布一些地段好，价格便宜的房源吸引人，当有人表示对此房有意向时，却推辞该房子已出租或者以别的借口向租房者推荐其他房源。为避免双方时间浪费，联系时最好直接询问对方此房源是否存在，或者明确表明自己的租房条件。<br>
                    2、谨防没有房产证的房源<br>
                    一些人冒充房东行骗，将租来快到期的房屋转租，或者将只短租几天的房源再出租，从中牟利。产权证是建立租赁关系的第一条件，要使租赁关系合理合法，出租人还要持房产所有证件到房地产管理部门办理房屋租赁证件，只有获得了租赁证，出租手续才完备。 所以租房者要认真核实房东的房产证或经纪人的代理合同。<br>
                    3、避免低价陷阱<br>
                    有些经纪人打着“免佣金”的幌子吸引顾客，等顾客看到满意的房子后又以免收佣金为理由提高租金，赚取租金差额。租房者可以在最开始询问经纪人：交易过程中需要交纳的费用和确定的租金；租房前，首先确认房屋安全，不能只图便宜，一定要认真检查房子是否存在安全隐患，是否为隔断间等等。现在很多房子被改造成小间出租，租房时最好对房内设施检查仔细，如门窗是否正常，下水管道是否畅通，有无漏水，电路布置是否危险等。交纳租金前，勿忘试用各种开关、水、电、煤气等，以免入住后出现不必要的麻烦。<br>
                    4、明确房子日后维护和退还押金问题<br>
                    租房者租赁房子后不仅有使用房子的权利，还有维护房内各种设备设施的义务，因此在签订合同之前要和房东或者经纪人确认房内的设备设施当前状态，并将其附在合同附件中，以免以后发生纠纷。<br>
                    租房者在签合同之前也要将房东或经纪人收取的费用及需要向相关部门交纳的费用了解清楚，最好在合同后附《费用清单》详细计算相关收费。例如：房租是否包取暖费，房内电表、水表入住时的度数，以后缴费的方式。在这个过程中，也要谨防经纪人除租金外收取其他费用。例如房间内没有有线电视，经纪人却要代房东收取已交的有线费用等。一般在第一次缴纳租金时，都要交一定比例的押金。此时需要和房东或经纪人确认押金返还时间和返还额度，为了避免恶意扣除押金，可能扣除的部分都可以通过《费用清单》确认清楚。；
                  </p>
                  <p>租房流程</p>
                  <p>
                    1、查看双方证件<br>
                    A．房主查看房客身份证，(可向公安局确认身份证)，并索取复印件做为合同附件；<br>
                    B. 房客查看房主产权证明(可向当地产交易所进行确认) ，房主身份证(可向公安局确认身份证) 及两证的统一(即产权证的产权人与身份证相同) ，并索取复印件做为合同附件，有些只有使用权的老公房，则要看房东的公房租赁证和身份证是否相符；<br>
                    合同签字(房主方)与产权证的产权人如果不相同需有产权人的代理委托书，即如果通过经纪人找房，需要查看对方的代理委托书。<br>
                    2、确认所有费用<br>
                    签订房屋租赁合同之前，查看水、气、电、电话费、物业费等是否已结清，询问这些费用是否包括在房租内，如果不包括，是每个月定额，还是按照实际使用的费用由承租人一方负担，如何缴纳等；<br>
                    签订合同之前，还要跟房东或者经纪人确认房内的设备设施当前的状态，折旧和损坏是否计入扣除押金的费用，并将其附在合同附件中。<br>
                    3、确认合同<br>
                    签订租赁合同时要仔细阅读合同条款，对于模糊内容要详加询问，并落实于纸质租房合同，交押金需要收据，确认扣除押金的的约定情况，退房时需凭收据拿回押金。<br>
                    4、明确违约责任<br>
                    违约情况如：出租人逾期交付房屋，租期结束承租人逾期退租等，可以每日按高于租金标准收取违约金；若出租人擅自收回房屋，或者承租人擅自退租，可约定一次性承担较高的违约金，也可以约定支付未使用租期的租金作为违约金。<br>
                    5、入住之前<br>
                    a.为了安全起见，入住之前最好跟邻居了解下房源之前的租住情况，要求房东或经纪人换锁<br>
                    b.跟房东或经纪人确认交房租的时间，以免因晚交房租而产生滞纳金<br>
                    c.检查门窗，试用各种开关、水、电、煤气等<br>
                    二手房买卖注意事项<br>
                    1、核查房屋手续是否齐全<br>
                    房产证是证明房主对房屋享有所有权的惟一凭证，没有房产证的房屋交易时对买受人来说有得不到房屋的极大风险。房主可能有房产证而将其抵押或转卖，即使现在没有将来办理取得后，房主还可以抵押和转卖。所以最好选择有房产证的房屋进行交易。<br>
                    2、核实产权是否清晰<br>
                    有些房屋有好多个共有人，如有继承人共有的、有家庭共有的、还有夫妻共有的，对此买受人应当和全部共有人签订房屋买卖合同。如果只是部分共有人擅自处分共有财产，买受人与其签订的买卖合同未在其他共有人同意的情况下一般是无效的。所以卖方必须是房屋的所有人，通过查验产权证和产权人身份证核实，确认无误即可。<br>
                    3、询问交易房屋是否在租<br>
                    有些二手房在转让时，存在物上负担，即还被别人租赁。如果买受人只看房产证，只注重过户手续，而不注意是否存在租赁时，买受人极有可能得到一个不能及时入住的或使用的房产。因为我国包括大部分国家均认可“买卖不破租赁”，也就是说房屋买卖合同不能对抗在先成立的租赁合同。这一点在实际中被很多买受人及中介公司忽视，也被许多出卖人利用从而引起较多纠纷。<br>
                    4、土地情况是否清晰<br>
                    二手房中买受人应注意土地的使用性质，看是划拨还是出让，划拨的土地一般是无偿使用，政府可无偿收回，出让是房主已缴纳了土地出让金，买受人对房屋享有较完整的权利;还应注意土地的使用年限，如果一个房屋的土地使用权仅有40年，房主已使用十来年，对于买受人来说是否还应该按同地段土地使用权为70年商品房的价格来衡量时，就有点不划算。<br>
                    5、关注房龄情况<br>
                    很多二手房经过炒房团的买卖，房本都是近几年的。再加上经纪人谈到房龄时模棱两可，二手房的真实房龄就需要买房人多加留意了。可以通过询问和观察周边环境大概估算出房龄。<br>
                    6、了解市政规划是否影响<br>
                    有些房主出售二手房可能是已了解该房屋在5到10年左右要面临拆迁，或者房屋附近要建高层住宅，可能影响采光、价格等市政规划情况，才急于出售，作为买受人在购买时应全面了解详细情况。<br>
                    7、确保房屋性质合法<br>
                    房改房、安居工程、经济适用房本身是一种福利性质的政策性住房，在转让时有一定限制，而且这些房屋在土地性质、房屋所有权范围上有一定的国家规定，买受人购买时要避免买卖合同与国家法律冲突。<br>
                    8、单位房屋侵权问题<br>
                    一般单位的房屋有成本价的职工住房，还有标准价的职工住房，二者土地性质均为划拨，转让时应缴纳土地使用费。再者，对于标准价的住房一般单位享有部分产权，职工在转让时，单位享有优先购买权。买受人如果没有注意这些可能会和房主一起侵犯单位的合法权益。<br>
                    9、核实房屋状况<br>
                    在看房的时候从以下几个方面入手了解： 房屋建筑状况、房屋小区状况、房屋权利情况。这些情况的核实，有利于控制好交易的实际成本和风险。<br>
                    10、多方面询问房源详细情况<br>
                    房东或者经纪人在介绍房源的时候，会尽量让买主选择性知道房屋的真实情况。例如对面的空地是建菜场还是建单位，房屋内发生过的事情等等。在购买二手房之前，可以多向小区物业和居民打听情况。<br>
                    11、明确交易程序<br>
                    二手房交易可分为看房、签约、按合同约定明确时间给首付款、过户、交房、给余款几大步骤。这几个步骤需要注意：保留好交易的证据、过户是一定要房产证上所有人都到场才可以过户。<br>
                    12、物管费用是否拖欠<br>
                    有些房主在转让房屋时，其物业管理费，电费以及三气(天然气、暖气、煤气)费用长期拖欠，且已欠下数目不小的费用，买受人不知情购买了此房屋，所有费用买受人有可能要全部承担。所以在交房时要查看清楚对方是否缴纳清了物业费、水电煤气费等一切费用。<br>
                    13、明确违约责任<br>
                    和卖方要明确严格的违约责任，是双方诚信履行二手房买卖合同的保障。<br>
                    14、避免中介违规行为<br>
                    有些中介公司违规提供中介服务，如在二手房贷款时，为买受人提供零首付的服务，即买受人所支付的全部购房款均可从银行骗贷出来。买受人以为自己占了便宜，岂不知如果被银行发现，所有的责任有可能自己都要承担。<br>
                    15、 避免在买房决定过程中抬高房价<br>
                    如今在房价不断上涨的背景下，经纪人在买房者有购买意向后，可能会突然涨价，并给买房人制造多人看房供不应求的氛围。这时候，买房者需冷静，因为很可能在放弃后，这套房源的价格又回落到最初的报价。<br>
                    16、检查合同约定是否明确<br>
                    二手房的买卖合同虽然不需像商品房买卖合同那么全面，但对于一些细节问题还应约定清楚，如：合同主体、权利保证、房屋价款、交易方式、违约责任、纠纷解决、签订日期等等问题均应全面考虑。避免不必要的纠纷。
                  </p>
                  <p>二手房买卖流程</p>
                  <p>
                    (1)、买卖双方建立信息沟通渠道，买方了解房屋整体现状及产权状况，要求卖方提供合法的证件，包括房屋所有权证书、身份证件及其它证件。<br>
                    (2)、如卖方提供的房屋合法，可以上市交易，买方可以交纳购房定金(交纳购房定金不是商品房买卖的必经程序)，买卖双方签订房屋买卖合同(或称房屋买卖契约)。买卖双方通过协商，对房屋坐落位置、产权状况及成交价格、房屋交付时间、房屋交付、产权办理等达成一致意见后，双方签订至少一式三份的房屋买卖合同。<br>
                    (3)、买卖双方共同向房地产交易管理部门提出申请，接受审查。买卖双方向房地产管理部门提出申请手续后，管理部门要查验有关证件，审查产权，对符合上市条件的房屋准予办理过户手续，对无产权或部分产权又未得到其他产权共有人书面同意的情况拒绝申请，禁止上市交易。<br>
                    (4)、立契。房地产交易管理部门根据交易房屋的产权状况和购买对象，按交易部门事先设定的审批权限逐级申报审核批准后，交易双方才能办理立契手续。现在北京市已取消了交易过程中的房地产卖契，即大家所俗称的“白契”。<br>
                    (5) 、缴纳税费。税费的构成比较复杂，要根据交易房屋的性质而定。比如房改房、危改回迁房、经济适用房与其它商品房的税费构成是不一样的。<br>
                    (6)、办理产权转移过户手续。交易双方在房地产交易管理部门办理完产权变更登记后，交易材料移送到发证部门，买方凭领取房屋所有权证通知单到发证部门申领新的产权证。<br>
                    (7)、对贷款的买受人来说在与卖方签订完房屋买卖合同后由买卖双方共同到贷款银行办理贷款手续，银行审核买方的资信，对双方欲交易的房屋进行评估，以确定买方的贷款额度，然后批准买方的贷款，待双方完成产权登记变更，买方领取房屋所有权证后，银行将贷款一次性发放。<br>
                    (8)、买方领取房屋所有权证、付清所有房款，卖方交付房屋并结清所有物业费后双方的二手房屋买卖合同全部履行完毕。
                  </p>
