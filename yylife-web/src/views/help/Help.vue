                  <p>若需要到外地面试、工作，外地对您来说，人生地不熟，在对单位没详细了解的情况下，要特别小心谨慎，不要冒然行动，谨防上当受骗。</p>
                  <p></p>
                  <p>提醒小贴士：个人在求职过程，多方面、多渠道详细了解公司情况及背景，看看公司是否正规，业务是否合法，单位是否拥有合法的营业执照和经营许可证，是否有投诉、不良记录等。</p>
