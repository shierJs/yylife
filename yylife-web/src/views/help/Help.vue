                  <p><strong>数码相机知识防骗科谱</strong></p>
                  <p>
                    了解水货DC <br>
                    水货DC是指导那些通过非正常渠道进入市场，没有经过厂商许可或是将原本应该在某地销售的产品转移到另外的地方进行销售的产品。而 并非大家所理解的走私货或者质量不过关、以坏充好的商品。水货由于没有关税之类的费用，所以价格非常的低廉，不但消费者买的水货便 宜，商家经营的利润空间也会更大一些。<br>
                    水货有两种：<br>
                    一种是从香港或者日本等地的渠道进货的叫“水货”，也称走私数码相机，这种水货数码相机不排除其为原厂正货的可能。但这种产品通 常不享有保修的售后服务，从市场暗访的情况来看，这种水货在一些地方占了一半以上，比如宁波、温州市场。<br>
                    还有一种水货是将别人用过的或劣质相机经过回收、翻新等手段进行伪装后，便成了正品，这种水货尤其值得注意，而有些所谓的“水货 ”实际上就是国内手工作坊改造的，普通消费者仅从外表上看往往很难区分，另外，也会出现相机机身是新的，但配件是旧货甚至是假货的 问题。一些奸商利用人们对数码产品不够了解的弱点，卖给你的水货表面上价格便宜，其实与行货相比并不便宜！因为有一些奸商将原厂出 品的配件调包，本把本来随机附送的原装电源、充电器、充电电池、存储卡等产品扣下来单卖，而把一些廉价、劣质的电源、充电器、电池 等产品赠送给你。最典型的是将电池换成假冒电池或兼容电池，或将原配的存储卡换成小容量或杂牌卡。这种水货有人称之为“深水货”， 消费者买了它可就有的烦了，在维修店里，经常有消费者拿着这种组装机来修。<br>
                    大家都知道，水货数码相机在国内多数是很难得到售后质保服务的，即便有个别品牌，如佳能，松下等通过一定的程序，还是可以得到保 修服务，但对这一般消费者来说很难办到。所以说我们选择了水货产品，在享受低廉价格的同时，也要承担将来可能发生的高额维修费用的 风险。因为水货在商家规定的保修时间内出现问题的话，一般只能拿回产是进行保修，但需要很长的时间和大量的金钱，另外一种方法是到 普通的维修点维修，这种维修方式并不专业，很难令人放心。而且自费数码相机维修的价格也是高的离谱。如果损坏的主要部件，如镜头、 主板等，那么你要付出的维修费用可能是你购买整机费用的一半甚至更高。<br>
                    另外，现在水货很大一部分是从网站来订购的，这种方式都是通过邮寄取得的，这样，如果碰到没法有信誉的网站，收到钱却不发货，即 使邮寄到了，如果与其宣传资料不符或产品根本就是坏的，那购买者的损失就大了。所以购买水货的人看起来省下了一些钱，可也需要承担 很大的风险。<br>
                    水货泛滥的原因<br>
                    既然购买水货有这么多危害，那么为什么它们还存在一定的市场呢？这其中主要有以下几个原因：<br>
                    难以辨别<br>
                    现在的水货产品 的制作工艺安全可以到以假乱真，加上有些水货本身就是原厂货，只是应销售的地点改变而成了水货，这种水货更是难以辨别。另一方面， 消费者的辨别能力有限，具备专业辨别水平的人更是瘳瘳无几。这就使得一些利欲熏心的奸商拿着水货当行货欺骗消费者。<br>
                    价格低廉<br>
                    像数码相机这种电子产品进关的时候往往要加上较高的关税，使国内零售市场上的产品价格一般都要比国外的价格高很多，比如在日本 的销售折算成人民币大约3000元，而在国内可能要卖到4000元，这对一些对价格非常敏感的朋友无疑是一个不小的诱惑。<br>
                    急于追新<br>
                    数码相机产品的新型号推出速度越来越快，在日本每个月都要推出好多新型号，由于受到地域及国内消费水平的限制，我们在国内市场上只 能看到有限的几种机型，新机型进入国内需要很长时间。对于喜欢求新求异的朋友，在几乎无法在国内及时地买到自己喜爱的最新产品时， 就会将目光转移到先行到货的水货身上。<br>
                    如何分辨水货<br>
                    一般来说，水货的价格要比行货低一些，但我们绝不能只从价格高低就断定哪个型号的DC是不是水货。由于水货数码相机大都不是在国内 生产的，往往没有中文说明书，说明书都是日文或者英文的（日本讲究环保，包装盒都是再生纸和简单印刷的），香港行货（我们称之为水 货）的说明书是繁体中文的。一般都是日文和繁体中文包装，驱动程序、软件等上面也没有中文。即使有的水货提供中文说明书，往往也是 印刷比较粗糙的翻印本。也有的经销商明确告诉你是水货，然后给你附上一本相似型号相机的中文说明书。行货的包装盒内还应带有一张保 修卡，而且卡上还要盖有合法的保修专用章。再大致地看一下数码相机外壳上的机身型号是否属于销往中国大陆市场的产品型号。具体销信 中国大陆市场的产品型号，可向当地数码相机特约维修中心查询，或直接拨打数码相机生产商的服务热线询问。识别产品是否为行货最有效 的办法是根据机身序列号来判别。由于每一台数码相机都具有一个唯一的编码，用户只需拨打厂家国内的技术支持部、分公司的电话或者厂 家提供的免费查询电话，就能知道自己购买的数码相机到底是不是行货了。从厂家授权的国内代理商处购买相机也是一个防止买到水货的好 办法。<br>
                    另外，仔细地对数码相机机身进行检验一下，要仔细检查机身、螺丝、液晶屏等部位，看是否划伤、刮花、灰尘等等，这也是必要的手段 。认真对镜头进行检查，因为镜头是数码相机中最为重要的组成部分，它的质量好坏直接关系到数码相机的整体性能，因此对镜头进行检查 是鉴别水货相机的很重要的一个环节。<br>
                    下面为大家具体介绍一下各品牌水货行货的辨别方法<br>
                    索尼<br>
