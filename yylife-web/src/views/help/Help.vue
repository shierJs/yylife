                  <p>第四章 附则</p>
                  <p>
                    附件一 招聘信息填写规范<br>
                    1.关于标题<br>
                    标题是所有信息内容主旨的全部体现，精确表达信息发布者的意图。言简意赅、表词达意的标题文字更容易受到潜在求职者的青睐。我们鼓励信息发布者如实、恰当、富有技巧地为信息内容拟一个合适的标题，同时不允许发布包括但不限于以下情形的标题：<br>
                    （1）含有违反国家法律法规以及国家法律法规明令禁止的标题描述；<br>
                    （2） 超出招聘类别允许发布信息的范围，与招聘行业信息无关的标题描述；<br>
                    （3）表达模糊、无法判断信息发布者意图的标题描述；<br>
                    （4）标题中含有无关符号、字母、数字等内容且影响浏览者判断其真实意图的标题描述；<br>
                    （5）夸大事实，容易对信息浏览者产生误导的标题描述。<br>
                    2.关于图片<br>
                    图片是浏览者最直观了解信息详情的入口，图片的质量影响浏览者对信息的判断，在一定程度上，浏览者的偏好受图片的左右。我们鼓励信息发布者上传真实、清晰、合理尺寸（如网站有规定按网站规定）的图片，同时不允许发布包括但不限于以下情形的图片：<br>
                    （1）图片内容含有违反国家法律法规以及国家法律法规明令禁止的内容；<br>
                    （2）图片内容与招聘的类别信息无关联；<br>
                    （3）未经他人许可，盗用、冒用他人的图片；<br>
                    （4）发布所有权存在纠纷、争议的图片；<br>
                    （5）图片模糊、难以辨认，影响浏览者对信息内容的判断；<br>
                    （6）上传多张与信息内容无关联的图片，影响浏览者对信息内容的判断；<br>
                    （7）图片中所含有的联系方式等内容占据大量的图片面积，影响浏览者对图片内容的判断；<br>
                    （8）图片中所含有的联系方式与信息所留联系方式不一致。<br>
                    3.关于联系方式<br>
                    联系方式在求职者和用人单位之间起纽带作用，电话号码、QQ、微信等主流通讯工具均属于联系方式的范畴，信息发布者在信息发布时可根据自己的意愿选择一种或多种联系方式进行填写。为了保证能够与信息发布者及时取得联系，我们要求真实、有效的电话号码为必填选项，同时不允许发布包括但不限于以下情形的联系方式：<br>
                    （1）盗用、冒用他人联系方式；<br>
                    （2）联系方式虚假、不存在（电话号码、QQ、微信虚假或不存在）；<br>
                    （3）联系方式不规范书写（用字母“o”代替数字“0”，用字母“l”代替数字“1”等情形）；<br>
                    （4）联系方式中含有非法含义的字符或拼音。<br>
                    4.关于联系人<br>
                    联系人是信息内容的发布者和信息意图的表达者，真实有效的联系人身份是信息质量的根基，信息发布者出于保护隐私的需要，可以使用姓氏与“先生”、“女士”、“老师”、“小姐”等称谓进行搭配，但必须保证用户的性别与其称谓相符合，避免对信息浏览者造成不必要的误解。信息发布者也可以根据自己的偏好，使用个性化的称谓或昵称。我们要求信息发者使用本人真实身份进行信息的发布，保证通过联系方式能够联系到信息发布者本人，同时不允许发布包括但不限于以下情形的联系方式：<br>
                    （1）含有违反国家法律法规以及国家法律法规明令禁止的词汇表述；<br>
                    （2）违反社会公序良俗，辱骂、诽谤他人的词汇表述；<br>
                    （3）隐晦、容易令人产生歧义的词汇表述；<br>
                    （4）对信息本身做广告性描述的词汇表述。<br>
                    5.关于内容<br>
                    内容是信息的主体部分，是对信息发布者所发布产品、服务的详尽描述。用户可从招聘职位的基本要求、工作职责、福利待遇（主要指保险和公积金缴纳情况）、工作环境、公司的发展状况、职位的发展前景等方面进行如实描述，用户也可根据自己的偏好，进行个性化语言描述。我们鼓励用户发布真实有效的信息内容，同时不允许发布包括但不限于以下情形的内容：<br>
                    （1）内容含有违反国家法律法规以及国家法律法规明令禁止的信息描述；<br>
                    （2）违反社会公序良俗的信息描述；<br>
                    （3）发布与招聘类别信息无关的信息描述；<br>
                    （4）虚假、伪造、夸大事实的信息描述；<br>
                    （5）表达隐晦、含糊、语意不清，容易让信息浏览者产生歧义的信息描述；<br>
                    （6）内容含有无关字符，对信息浏览者正常浏览信息造成干扰的信息描述。<br>
                    6.关于薪酬<br>
                    薪酬是员工因向所在的组织提供劳务而获得的各种形式的酬劳，这里指的薪酬指货币和可以转化为货币的报酬，即薪资。我们要求信息发布者如实表达自己或企业能够提供的薪资范围，不允许通过虚假薪资对求职者进行误导。标注的薪资是信息发布者所能够提供的薪资范围与所在地该行业同期薪资范围的统一，任何违反人力资源和社会保障部关于劳动者合法权益保护规定、扰乱劳动人才市场秩序的行为，我们有理由怀疑其动机是否超出市场主体的正当经济行为。我们欣赏用人单位能够通过其丰厚的待遇和可观的发展前景招募优秀的员工，也希望求职者能够凭借自身的技能和智慧找到满意的工作，同时不允许发布包括但不限于以下情形的薪资内容：<br>
                    （1） 标注薪资与实际薪资偏离一般劳动薪酬波动范围（双方另有约定除外）；<br>
                    （2）薪资表述模糊，薪酬数目与工作劳动时间单位不匹配；<br>
                    （3）薪酬项中的薪资表述与其他项中的薪资表述不一致；<br>
                    （4）不规范的薪资书写方式（用字母“o”代替数字“0”，用字母“l”代替数字“1”等情形）。<br>
                    7.关于地址<br>
                    地址是指信息发布者的所在地或产品、服务的所在地，涉及到招聘类别，特指用人单位、面试地点、工作场所的具体地址。我们要求信息发布者对地址项进行详实描述，不伪造地址、不冒用地址，用人单位、面试地点、工作场所的地址必须精确到街道的方向、楼层、门牌号，同时不允许发布包括但不限于以下情形的地址信息：<br>
                    （1）盗用、冒用他人地址；<br>
                    （2）描述的地址虚假。<br>
