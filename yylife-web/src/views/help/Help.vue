                    （一）招聘信息中，关键内容（含企业名称、职位名称、职位类别、工作地址、联系方式、薪酬福利等）填写虚假；<br>
                    （二）发布销售、推广、转让等与建立劳动关系无关的信息或服务；<br>
                    （三）招聘与公司性质以及经营范围无关联的职位；<br>
