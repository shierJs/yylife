                    第十二条 发布包括但不限于以下列示的无意义信息（对浏览者无帮助信息），根据情节轻重进行以下一种或多种处理：降权违规信息、返回修改违规信息、删除所有违规内容等。<br>
                    （一）故意发布与招聘类别不相关的职位；<br>
